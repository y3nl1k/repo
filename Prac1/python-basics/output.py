#Each call prints text on a new line by default:
print("Hello World!")
print("I am learning Python.")
print("It is awesome!")


#You can use either " double quotes or ' single quotes:
print("This will work!")
print('This will also work!')


#If you want to print multiple words on the same line
print("Hello World!", end=" ")
print("I will print on the same line.")


#we don't put numbers inside double quotes:
print(3)
print(358)
print(50000)


#do math inside the print() function
print(3 + 3)
print(2 * 5)

